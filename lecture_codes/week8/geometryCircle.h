#define PI 3.15


double circumferenceOfCircle(double radius)
{
    return 2* PI*radius;
}

double areaOfCircle(double radius)
{
    return PI*radius*radius;
}


double volumeOfSphere(double  radius)
{
    return (4.0/3.0)*PI*radius*radius*radius;
}
